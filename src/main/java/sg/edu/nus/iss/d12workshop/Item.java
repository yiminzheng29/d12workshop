package sg.edu.nus.iss.d12workshop;

public class Item {
    
    private String itemName;
    private Integer quantity;

    // getter and setters
    public String getItemName() { 
        return itemName;
    }
    public void setItemName (String n) {
        itemName = n;
    }
    public Integer getQuantity() {
        return quantity;
    }
    public void setQuantity(Integer i) {
        quantity = i;
    }

    
}
